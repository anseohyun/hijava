
public class Variable {

	public static void main(String[] args) {
		// 변수: 단 하나의 값을 저장할 수있는 메모리 공간
		// 선언 : 메모리공간 할당
		// 변수 선언: 데이터하나를 저장하는 공간 
		
		// 자료형 (DataType)
		// - 정수 integer
		
		// 정수 10을 저장하는 공간 
		int number = 10;
		System.out.println(number);
		
		// 실수를 저장하는 공간
		double number2 = 10.25;
		System.out.println(number2);
		
		// 문자 여러개를 저장하는 공간: String 
		String str = "Java";
		// str공간에 자바라는 여러개의 문자를 저장했다.
		System.out.println(str);		
		
		// 참과 거짓을 저장하는 공간 
		boolean result = true;
		System.out.println(result);
		
		
	}	
}
